"""
Determinism: golden-trajectory test.

Two runs with the same (seed, scenario) must produce identical
KPI/event sequences (06-digital-twin.md §13, GR10, TP2).

The golden hash is generated by the first run and asserted by all
subsequent runs. To regenerate: run with --update-golden flag.

Owning docs: 06-digital-twin.md §21, 16-testing.md §7
"""
from __future__ import annotations

import hashlib
import json

import pytest

from app.domain.twin.events import KpiUpdatedEvent, NfFailedEvent
from app.domain.twin.network_twin import NetworkTwin
from app.infrastructure.rng.rng import RngService

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
SEED = 42
N_TICKS = 20


def _run_twin(seed: int, n_ticks: int) -> str:
    """Run a twin for n_ticks and return a stable hash of the trajectory."""
    rng_svc = RngService(seed=seed)
    twin = NetworkTwin.from_baseline(seed=seed)
    all_events: list[dict] = []

    for tick in range(1, n_ticks + 1):
        stream = rng_svc.for_tick(tick)
        events = twin.advance(stream, tick=tick)
        for e in events:
            record = {
                "tick": tick,
                "type": e.type.value if hasattr(e.type, "value") else str(e.type),
                "entity": getattr(e, "entity_id", None),
            }
            all_events.append(record)

    payload = json.dumps(all_events, sort_keys=True, default=str)
    return hashlib.sha256(payload.encode()).hexdigest()


# ---------------------------------------------------------------------------
# Tests
# ---------------------------------------------------------------------------
class TestGoldenTrajectory:
    def test_two_runs_same_seed_same_hash(self) -> None:
        """Core determinism assertion: identical seed → identical hash."""
        h1 = _run_twin(seed=SEED, n_ticks=N_TICKS)
        h2 = _run_twin(seed=SEED, n_ticks=N_TICKS)
        assert h1 == h2, "Non-determinism detected: two runs produced different hashes"

    def test_different_seed_different_hash(self) -> None:
        """Different seed → different trajectory."""
        h1 = _run_twin(seed=42, n_ticks=N_TICKS)
        h2 = _run_twin(seed=99, n_ticks=N_TICKS)
        assert h1 != h2

    def test_trajectory_contains_events(self) -> None:
        """The twin must produce events — not a silent empty run."""
        rng_svc = RngService(seed=SEED)
        twin = NetworkTwin.from_baseline(seed=SEED)
        total_events = 0
        for tick in range(1, N_TICKS + 1):
            events = twin.advance(rng_svc.for_tick(tick), tick=tick)
            total_events += len(events)
        assert total_events > 0, "No events emitted across 20 ticks — check twin.advance()"

    def test_kpi_updated_events_present(self) -> None:
        """KPI_UPDATED events must appear — confirms advance() is running models."""
        rng_svc = RngService(seed=SEED)
        twin = NetworkTwin.from_baseline(seed=SEED)
        kpi_events = []
        for tick in range(1, N_TICKS + 1):
            for e in twin.advance(rng_svc.for_tick(tick), tick=tick):
                if isinstance(e, KpiUpdatedEvent):
                    kpi_events.append(e)
        assert len(kpi_events) > 0, "No KpiUpdatedEvents — NF models not running"

    def test_low_rng_produces_failures(self) -> None:
        """
        With a fixed RNG returning 0.0 (below all hazard thresholds),
        every NF must fail on the first tick.
        """
        class ZeroRng:
            def random(self) -> float: return 0.0
            def gauss(self, mu: float, sigma: float) -> float: return mu
            def uniform(self, lo: float, hi: float) -> float: return (lo + hi) / 2

        twin = NetworkTwin.from_baseline(seed=42)
        events = twin.advance(ZeroRng(), tick=1)  # type: ignore[arg-type]
        fail_events = [e for e in events if isinstance(e, NfFailedEvent)]
        assert len(fail_events) > 0, "ZeroRng should trigger failures"
